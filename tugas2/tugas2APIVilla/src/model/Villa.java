package model;

public class Villa {
    public int id;
    public String nama;
    public String lokasi;
    public int hargaPerMalam;

    public Villa(int id, String nama, String lokasi, int hargaPerMalam) {
        this.id = id;
        this.nama = nama;
        this.lokasi = lokasi;
        this.hargaPerMalam = hargaPerMalam;
    }
}
